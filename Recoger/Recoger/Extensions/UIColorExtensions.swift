import UIKit

extension UIColor {
	static var bitBlueColor: UIColor {
		return UIColor(red: 60/255, green: 160/255, blue: 220/225, alpha: 1.0)
	}

	static var defaultKeyboardBackgroundColor: UIColor {
		return UIColor(red: 204/255, green: 208/255, blue: 215/255, alpha: 1.0)
	}
}
